#include "player/player_state.h"

void PlayerState::Reset() {
  medias_->medias().clear();
  index_ = 0;
  is_playing_ = false;
  is_valid_ = true;
  is_seekable_ = true;
  is_completed_ = false;
  position_ = 0;
  duration_ = 0;
  is_started_ = false;
}
